// ── Helper to pick data by chart + gender ────────────────────

export type ChartType = 'weight_for_age' | 'height_for_age' | 'head_circumference'

export function getPercentileData(
  chart: ChartType,
  gender: 'male' | 'female',
  standard: 'who' | 'cdc'
): PercentilePoint[] {
  if (standard === 'who') {
    if (chart === 'weight_for_age') return gender === 'male' ? WHO_WEIGHT_BOYS : WHO_WEIGHT_GIRLS
    if (chart === 'height_for_age') return gender === 'male' ? WHO_HEIGHT_BOYS : WHO_HEIGHT_GIRLS
    return gender === 'male' ? WHO_HC_BOYS : WHO_HC_GIRLS
  }
  // CDC (no head circ for CDC — fallback to WHO)
  if (chart === 'weight_for_age') return gender === 'male' ? CDC_WEIGHT_BOYS : CDC_WEIGHT_GIRLS
  if (chart === 'height_for_age') return gender === 'male' ? CDC_HEIGHT_BOYS : CDC_HEIGHT_GIRLS
  return gender === 'male' ? WHO_HC_BOYS : WHO_HC_GIRLS
}

